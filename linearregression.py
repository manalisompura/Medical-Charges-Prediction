#apart from a few exceptions, the points seem to form a line. we will try and fit a line using this points and use the line to predict charges for a given age. equation of line : y = wx + b
#we're assume the following relationship between the two: charges = w*age + b

def estimate_charges(age,w,b):
   return ages * w + b

w=50
b=100

ages = non_smoker_df.age
estimated_charges = estimate_charges(ages,w,b)

estimated_charges
